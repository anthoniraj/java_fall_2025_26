package edu.vit.corejava.operators;
/**
 * ArithmeticOperators demonstrates the use of basic arithmetic operators in Java.
 * Arithmetic operators are used to perform basic mathematical operations such as addition, subtraction,
 * multiplication, division, and modulus.
 * @author Anthoniraj Amalanathan
 * @version 1.0
 * @since 2025-07-28
 */

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 10, b = 5;
        System.out.println("Addition: " + (a + b)); // Output: 15
        System.out.println("Subtraction: " + (a - b)); // Output: 5
        System.out.println("Multiplication: " + (a * b)); // Output: 50
        System.out.println("Division: " + (a / b)); // Output: 2
        System.out.println("Modulus: " + (a % b)); // Output: 0
    }
}
